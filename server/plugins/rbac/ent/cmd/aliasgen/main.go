// Package main generates the RBAC plugin's owned Ent import-surface aliases.
package main

import (
	"fmt"
	"os"
	"path/filepath"
)

const (
	aliasDirPerm  = 0o750
	aliasFilePerm = 0o600
)

func main() {
	workingDir, err := os.Getwd()
	if err != nil {
		fail("resolve working directory", err)
	}

	files := map[string]string{
		filepath.Join("permission", "generated_alias.go"):     permissionAliasSource,
		filepath.Join("role", "generated_alias.go"):           roleAliasSource,
		filepath.Join("rolepermission", "generated_alias.go"): rolePermissionAliasSource,
		filepath.Join("userrole", "generated_alias.go"):       userRoleAliasSource,
	}

	for relativePath, contents := range files {
		absPath := filepath.Join(workingDir, relativePath)
		if err := os.MkdirAll(filepath.Dir(absPath), aliasDirPerm); err != nil {
			fail(fmt.Sprintf("create directory for %s", relativePath), err)
		}
		if err := os.WriteFile(absPath, []byte(contents), aliasFilePerm); err != nil {
			fail(fmt.Sprintf("write %s", relativePath), err)
		}
	}
}

func fail(action string, err error) {
	fmt.Fprintf(os.Stderr, "%s: %v\n", action, err)
	os.Exit(1)
}

