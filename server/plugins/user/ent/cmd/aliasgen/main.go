// Package main generates the user plugin's owned Ent import-surface aliases.
package main

import (
	"fmt"
	"os"
	"path/filepath"
)

const (
	aliasDirPerm  = 0o750
	aliasFilePerm = 0o600
)

func main() {
	workingDir, err := os.Getwd()
	if err != nil {
		fail("resolve working directory", err)
	}

	files := map[string]string{
		filepath.Join("user", "generated_alias.go"):           userAliasSource,
		filepath.Join("refreshsession", "generated_alias.go"): refreshSessionAliasSource,
	}

	for relativePath, contents := range files {
		absPath := filepath.Join(workingDir, relativePath)
		if err := os.MkdirAll(filepath.Dir(absPath), aliasDirPerm); err != nil {
			fail(fmt.Sprintf("create directory for %s", relativePath), err)
		}
		if err := os.WriteFile(absPath, []byte(contents), aliasFilePerm); err != nil {
			fail(fmt.Sprintf("write %s", relativePath), err)
		}
	}
}

func fail(action string, err error) {
	fmt.Fprintf(os.Stderr, "%s: %v\n", action, err)
	os.Exit(1)
}

const userAliasSource = `// Code generated by go generate ./plugins/user/ent; DO NOT EDIT.

package user

import (
	internalpredicate "graft/server/internal/ent/predicate"
	internaluser "graft/server/internal/ent/user"
)

const (
	FieldID       = internaluser.FieldID
	FieldUsername = internaluser.FieldUsername
)

func IDEQ(id int) internalpredicate.User {
	return internaluser.IDEQ(id)
}

func UsernameEQ(username string) internalpredicate.User {
	return internaluser.UsernameEQ(username)
}
`

