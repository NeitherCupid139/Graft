CREATE TABLE IF NOT EXISTS announcements (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  title text NOT NULL,
  content text NOT NULL,
  level character varying NOT NULL,
  status character varying NOT NULL,
  pinned boolean NOT NULL DEFAULT false,
  publish_at timestamptz NULL,
  expire_at timestamptz NULL,
  created_by bigint NULL,
  updated_by bigint NULL,
  deleted_by bigint NULL,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW(),
  deleted_at bigint NOT NULL DEFAULT 0,
  PRIMARY KEY (id),
  CONSTRAINT announcements_level_check CHECK (level IN ('info', 'warning', 'success', 'error')),
  CONSTRAINT announcements_status_check CHECK (status IN ('draft', 'published', 'archived'))
);

CREATE INDEX IF NOT EXISTS announcements_admin_status_level_updated
  ON announcements (deleted_at, status, level, updated_at DESC, id DESC);
CREATE INDEX IF NOT EXISTS announcements_admin_pinned_updated
  ON announcements (deleted_at, pinned DESC, updated_at DESC, id DESC);
CREATE INDEX IF NOT EXISTS announcements_user_visible
  ON announcements (pinned DESC, publish_at DESC, id DESC)
  WHERE deleted_at = 0 AND status = 'published';
CREATE INDEX IF NOT EXISTS announcements_admin_keyword
  ON announcements
  USING GIN (to_tsvector('simple', title || ' ' || content))
  WHERE deleted_at = 0;

CREATE TABLE IF NOT EXISTS announcement_reads (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  announcement_id bigint NOT NULL,
  user_id bigint NOT NULL,
  read_at timestamptz NOT NULL DEFAULT NOW(),
  created_at timestamptz NOT NULL DEFAULT NOW(),
  PRIMARY KEY (id),
  CONSTRAINT announcement_reads_announcement_id_fkey
    FOREIGN KEY (announcement_id) REFERENCES announcements (id) ON DELETE CASCADE
);

CREATE UNIQUE INDEX IF NOT EXISTS announcement_reads_announcement_user
  ON announcement_reads (announcement_id, user_id);
CREATE INDEX IF NOT EXISTS announcement_reads_user_read_at
  ON announcement_reads (user_id, read_at DESC, announcement_id);

COMMENT ON TABLE announcements IS '公告中心公告主表';
COMMENT ON COLUMN announcements.id IS '公告记录主键';
COMMENT ON COLUMN announcements.title IS '公告标题';
