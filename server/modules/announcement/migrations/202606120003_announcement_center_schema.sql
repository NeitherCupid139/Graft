COMMENT ON COLUMN announcements.level IS '公告级别 typed contract，取值为 info、warning、success、error';
COMMENT ON COLUMN announcements.status IS '公告状态 typed contract，取值为 draft、published、archived';
COMMENT ON COLUMN announcements.pinned IS '是否置顶，true 表示用户侧和管理侧优先展示';
COMMENT ON COLUMN announcements.publish_at IS '公告开始对用户可见的时间，空值表示发布时立即生效';
COMMENT ON COLUMN announcements.expire_at IS '公告对用户可见的截止时间，空值表示不过期';
COMMENT ON COLUMN announcements.created_by IS '创建公告的管理员用户 ID';
COMMENT ON COLUMN announcements.updated_by IS '最近更新公告的管理员用户 ID';
COMMENT ON COLUMN announcements.deleted_by IS '软删除公告的管理员用户 ID';
COMMENT ON COLUMN announcements.created_at IS '公告记录创建时间';
COMMENT ON COLUMN announcements.updated_at IS '公告记录最近更新时间';
COMMENT ON COLUMN announcements.deleted_at IS '公告软删除时间戳，0 表示未删除';

COMMENT ON TABLE announcement_reads IS '公告中心用户已读事实表';
COMMENT ON COLUMN announcement_reads.id IS '公告已读事实主键';
